
public class FiveAdditionAverage {
	public static void main(String[] args) {
		int marathi = 82;
		int hindi = 80;
		int english =  70;
		int history = 65;
		int geography = 50;
		int total = marathi + hindi + english + history + geography;
		int avr = total/5;
		
		System.out.println("Total marks of five subjects are " + total);
		System.out.println("Average of five subjects are "+ avr);
		
		}

	
}
